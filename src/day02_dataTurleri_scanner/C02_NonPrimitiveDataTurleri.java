package day02_dataTurleri_scanner;

public class C02_NonPrimitiveDataTurleri {
    public static void main(String[] args) {

        int sayi = 10;

        String str = "jAVA candır";

        System.out.println(sayi); // 10

        System.out.println(str.toLowerCase()); // küçük harfe çevirir
        System.out.println(str.toUpperCase()); // büyük harfe çevirir

        /*
             1- primitive data turleri sadece deger saklayabilir
                non-primitive data turleri ise deger saklamanin yaninda
                hazir method'lar da barindirir
                bu hazir method'lar sayesinde biz tanimlanmis islemleri kolayca yapabiliriz
             2- primitive data turlerinin isimleri kucuk harf ile baslar
                non-primitive data turlerinin isimleri buyuk harf ile baslar
         */



    }
}
